import React from 'react';

interface NavigationItem {
  id: string;
  label: string;
  icon: React.ReactNode;
  activeIcon?: React.ReactNode;
  badge?: number;
}

interface MobileBottomNavigationProps {
  items: NavigationItem[];
  activeItem: string;
  onItemSelect: (itemId: string) => void;
  className?: string;
}

const MobileBottomNavigation: React.FC<MobileBottomNavigationProps> = ({
  items,
  activeItem,
  onItemSelect,
  className = ''
}) => {
  return (
    <div className={`fixed bottom-0 left-0 right-0 bg-white dark:bg-gray-800 border-t border-gray-200 dark:border-gray-700 safe-area-pb z-50 ${className}`}>
      <div className="flex items-center justify-around px-2 py-2">
        {items.map((item) => {
          const isActive = activeItem === item.id;
          
          return (
            <button
              key={item.id}
              onClick={() => onItemSelect(item.id)}
              className={`flex flex-col items-center justify-center min-w-0 flex-1 px-2 py-2 rounded-xl transition-all duration-200 ${
                isActive
                  ? 'text-blue-600 dark:text-blue-400 bg-blue-50 dark:bg-blue-900/20'
                  : 'text-gray-600 dark:text-gray-400 hover:text-gray-900 dark:hover:text-white'
              }`}
              style={{ minHeight: '60px' }}
            >
              <div className="relative mb-1">
                {isActive && item.activeIcon ? item.activeIcon : item.icon}
                {item.badge && item.badge > 0 && (
                  <div className="absolute -top-2 -right-2 w-5 h-5 bg-red-500 text-white text-xs font-bold rounded-full flex items-center justify-center">
                    {item.badge > 99 ? '99+' : item.badge}
                  </div>
                )}
              </div>
              <span className={`text-xs font-medium truncate max-w-full ${
                isActive ? 'text-blue-600 dark:text-blue-400' : ''
              }`}>
                {item.label}
              </span>
            </button>
          );
        })}
      </div>
    </div>
  );
};

